package myobj.Pirate;

public class MrTong2 {

	// true  : 해당 구멍은 찔린적 있음
	// false : 해당 구멍은 아직 찔린적 없음
	boolean[] stabbed;
	boolean[] launch;
		
	// launched가 true면 통아저씨가 발사된 상태
	public boolean launched;
	
	public MrTong2(int size) {
		stabbed = new boolean[size];
		launch = new boolean[size];		
		
		// 정답을 우선 1 ~ 4개 생성
		for (int i = 0,len = (int)(Math.random() * 4 + 1); i < len; i++) {
			launch[i] = true;
		}
		// 1000번 섞음
		for (int i = 0; i < 1000; i++) {
			int x = (int)(Math.random() * size);
			int y = (int)(Math.random() * size);
				
			boolean temp = launch[x];
			launch[x] = launch[y];
			launch[y] = temp;
		}
	}
	
	public boolean stab(int index) {
		if (stabbed[index]) {
			return false;
		} else {
			stabbed[index] = true;
			launched = launch[index];
		//	ex) launched = launch[index];를 아래문장을 한줄로
			
			//	if (launch[index] == true) {
			//		launched = true;
			//	} else {
			//		launched = false;
			//	}
			return true;
		}
	}
		
	public boolean[] getStabbed() {
		return stabbed;
	}
}